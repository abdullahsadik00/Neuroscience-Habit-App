import '../models/models.dart';

class HabitTemplate {
  final String id;
  final String title;
  final String anchorCue;
  final String action;
  final String reward;
  final HabitCategory category;
  final String timing; // morning | anytime | evening
  final String energyRequired; // low | medium | high
  final String duration; // 2min | 5min | 10min | 15min | 30min
  final String description;
  final List<FailureStyle> failureStyles;
  final List<PrimaryBlocker> primaryBlockers;
  final List<PeakEnergyWindow> peakEnergyWindows;
  final List<CoreDriver> coreDrivers;
  final List<String> neurochemTarget;
  final String? liteVersionId;

  const HabitTemplate({
    required this.id,
    required this.title,
    required this.anchorCue,
    required this.action,
    required this.reward,
    required this.category,
    required this.timing,
    required this.energyRequired,
    required this.duration,
    required this.description,
    this.failureStyles = const [],
    this.primaryBlockers = const [],
    this.peakEnergyWindows = const [],
    this.coreDrivers = const [],
    this.neurochemTarget = const [],
    this.liteVersionId,
  });
}

